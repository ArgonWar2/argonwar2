		<h4>Regno Chunjo</h4>
		<p>Il Regno Chunjo comprende la zona ad <strong>Ovest</strong> del continente. È un Regno <strong>teocratico</strong> governato da un capo spirituale.<br><br>
		Il regno è stato creato da Yoon-Young, cugino dell'ultimo Imperatore. Sua moglie, dotata di potenti forze magiche, lo ha aiutato a prevedere molto presto il pericolo causato dalla roccia Metin. Yoon-Young ha più volte consigliato di prendere provvedimenti, ma è sempre stato ignorato. Ha deciso quindi, assieme ai suoi sudditi, di ribellarsi contro l'allora intero Impero. Dopo il disfacimento dell'Impero, il Regno si trova ancora in <strong>aperto conflitto</strong> con l'Est ed in continua lotta con il Sud.<br><br>
		Gli abitanti del Regno Chunjo mirano alla supremazia sull'intero continente, così da poter combattere uniti la malvagia forza oscura della roccia Metin.</p>
