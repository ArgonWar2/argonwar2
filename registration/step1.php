		<h2>Regno Shinsoo</h2>
		<p>Il Regno Shinsoo è situato a <strong>Sud</strong> del continente. L'attività principale dei suoi abitanti è il <strong>commercio</strong>.<br><br>
		Sorto dopo la caduta dell'Impero fondato da Yoon-Yoing, è fiorito velocemente grazie agli ottimi rapporti commerciali con l'Est. Con l'Ovest invece gli abitanti vivono in continua lotta ed il commercio è interrotto. Coscienti del pericolo causato dalla roccia Metin che minaccia la loro esistenza, i mercanti hanno deciso di armarsi.<br><br>
		I loro obiettivi sono di riuscire ad arginare gli <strong>attacchi da Ovest</strong>, riaprire la via del commercio e diventare i dominatori di tutto il Regno.</p>
